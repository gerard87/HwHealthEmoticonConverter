        if (emojiAsciiMap == null) {
            emojiAsciiMap = new EmojiAsciiMap();
        }
        for (Map.Entry entry : emojiAsciiMap.entrySet()) {
            text = text.replaceAll((String) entry.getKey(), " " + entry.getValue() + " ");
        }
        text = text.trim().replaceAll("\\s{2,}", " ");
        return text;
